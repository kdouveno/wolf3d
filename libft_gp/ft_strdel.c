#include "libft.h"

void	ft_strdel(char **as)
{
	int		cmpt;

	if (as)
	{
		cmpt = 0;
		while (as[0][cmpt])
		{
			as[0][cmpt] = '\0';
			cmpt++;
		}
		free(*as);
		*as = NULL;
	}
}
