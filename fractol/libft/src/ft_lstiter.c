#include "../includes/libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	int		cmpt;

	cmpt = 0;
	while (lst)
	{
		(*f)(lst);
		lst = lst->next;
	}
}
