int		ft_sqrt(int nb)
{
	int a;
	int b;
	int i;

	a = 0;
	b = nb / 2;
	i = nb / 4;
	while (nb != i * i)
	{
		if (b - a <= 1)
			return (a);
		if (nb > i * i && i <= 46340)
			a = i;
		else
			b = i;
		i = (a + b) / 2;
	}
	return (i);
}
